package basic;

public class Datatype {

	public static void main(String[] args) {
		int num = 5;
		float f = 1.99f;
		char c = 'd';
		boolean b = true;
		String s = "HELLO";
		
		System.out.println(num);
		System.out.println(f);
		System.out.println(c);
		System.out.println(b);
		System.out.println(s);
	}

}
